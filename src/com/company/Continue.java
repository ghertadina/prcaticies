package com.company;

public class Continue {
    public static void main(String[] args) {

        for(int i = 0 ; i <=30 ; i ++){
            if(i%2 == 0){
                System.out.println(i+" это четное число");
            }else {
                System.out.println(i+ " не четное число");
            }
        }
    }
}
